ValidateKing    Proc FAR ;al = row cl = col si = player di = cell
        MOV AL,playerRows[SI]
        MOV CL,PlayerCols[SI]
        ;LEFT CELL
        PUSH DI
                CMP CL,1 
                JC GO ;IF COLUMN=0 it'll be negative then it's invalid
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                SUB DI,1
                call Validatemovetheking
        ;RIGHT CELL                 
        GO:
        POP DI
        PUSH DI
        PUSH CX
                ADD CL,1
                PUSH BX
                MOV BL,8
                CMP BL,CL
                JE GO1 
                ;IF CL NOT EQUAL 8 THEN WE STILL IN RANGE OF 0 TO 7 COLUMNS --> IT'S A VALID CELL
                add di,1
                call Validatemovetheking        
        ;UP CELL
        GO1:
        POP BX
        POP CX
        POP DI
        PUSH DI
                CMP DI,8
                JC GO2
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                sub di,8
                call Validatemovetheking
        ;DOWN CELL    
        GO2:
        POP DI
        PUSH DI
                ADD DI,8
                PUSH BX
                MOV BX,63 ;LAST CELL INDEX
                CMP BX,DI
                JC GO3
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                call Validatemovetheking
        GO3:
        POP BX
        POP DI
        ;LEFT UP CELL
        PUSH DI
                CMP CL,1 
                JC GO4 ;IF COLUMN=0 it'll be negative then it's invalid 
                CMP AL,1
                JC GO4 ;IF ROW=0 it'll be negative then it's invalid
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                sub di,9
                call Validatemovetheking
        GO4:
        POP DI
        PUSH DI
        ;RIGHT DOWN
        push cx
                ADD CL,1
                PUSH BX
                MOV BL,7
                CMP BL,CL;LAST COLUMN NO RIGHT
                JC GO5
                POP BX
                ADD DI,9
                PUSH BX
                MOV BX,63 ;LAST CELL INDEX
                CMP BX,DI
                JC GO5 ;LAST ROW NO DOWN
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                call Validatemovetheking
        GO5:
        POP BX
        pop cx
        POP DI
        PUSH DI
        ;LEFT DOWN
                CMP CL,0 ;first column no left ;;;not working 
                JE GO6
                CMP AL,7
                JE GO6 
                ;NOT LAST ROW
                ADD DI,7
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                call Validatemovetheking
                
        GO6: 
        POP DI
        ;RIGHT UP CELL
        PUSH DI
        push cx
                ADD CL,1
                PUSH BX
                MOV BL,7
                CMP BL,CL;LAST COLUMN NO RIGHT
                JC GO7
                CMP AL,1
                JC GO7 ;IF ROW=0 it'll be negative then it's invalid
                ;IF CARRY NOT 1 THEN IT'S A VALID CELL
                sub di,7 
                call Validatemovetheking
        GO7:               
        POP DI
        pop cx
        POP BX
                RET
ValidateKing    ENDP
